    private void btnRemoveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoveActionPerformed

        int selectedRow = tableUserAccount.getSelectedRow();
        if (selectedRow >= 0 )
        {
            int dialogResult = JOptionPane.showConfirmDialog(this, "Are you sure you want to remove?","Warning", JOptionPane.YES_NO_OPTION);
            if (dialogResult == JOptionPane.YES_OPTION)
            {
                UserAccount ua = (UserAccount) tableUserAccount.getValueAt(selectedRow, 0);
                
                if (ua.getRole().equals(UserAccount.ADMIN_ROLE))
                {
                    JOptionPane.showMessageDialog(this, "Admin account cant be delected", "Error", JOptionPane.ERROR_MESSAGE);
                    return;
                    
                }
                businesse.getUserAccountDirectory().deleteUserAccount(ua);
                JOptionPane.showMessageDialog(this, "User Acount delected", "Info", JOptionPane.INFORMATION_MESSAGE);
            
                refreshTable();
            }
        }
        else
        {
            JOptionPane.showMessageDialog(this, "Please select a row from table", "Error", JOptionPane.ERROR_MESSAGE);
            
        }








        // TODO add your handling code here:
    }//GEN-LAST:event_btnRemoveActionPerformed

