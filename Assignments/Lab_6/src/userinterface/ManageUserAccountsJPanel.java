    public void refreshTable()
    {
        DefaultTableModel dtm = (DefaultTableModel) tableUserAccount.getModel();
        dtm.setRowCount(0);
        
        for(UserAccount ua: businesse.getUserAccountDirectory().getUserAccountList())
        {
            Object row[] = new Object[3];
            row[0] = ua;
            row[1] = ua.getUserName();
            row[2] = ua.getRole();
            dtm.addRow(row);
            
                    
        }
        
    }

